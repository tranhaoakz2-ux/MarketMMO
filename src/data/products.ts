  reviewCount: number;
  stock: number;
  sold: number;
  views: number;
  seller: string;
  sellerLevel: number;
  verified: boolean;
  hot?: boolean;
  /** Chỉ có khi fetch qua getProductBySlugDb (trang chi tiết) — seed/mock data không có seller thật. */
  sellerLastActiveAt?: string | null;
  sellerInsuranceBalance?: number;
